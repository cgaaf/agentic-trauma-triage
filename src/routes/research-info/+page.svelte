		<section>
			<h2 class="mb-2 text-lg font-medium text-foreground">Why can't it be used clinically?</h2>
			<ul class="list-disc space-y-2 pl-5">
				<li>
					<strong>Not HIPAA compliant</strong> — No data encryption, access controls, or business
					associate agreements are in place.
				</li>
				<li>
					<strong>Not FDA approved</strong> — The system has not been validated or cleared for use in
					clinical decision-making.
				</li>
				<li>
					<strong>No audit trail</strong> — Patient data entered into the system is not securely
					stored or logged.
				</li>
				<li>
					<strong>No clinical validation</strong> — Outputs have not been validated against real
					clinical outcomes.
				</li>
			</ul>
		</section>

		<section>
			<h2 class="mb-2 text-lg font-medium text-foreground">What can you do with it?</h2>
			<p>
				You are welcome to use this tool with <strong>fictional or synthetic scenarios</strong> to
				explore how automated trauma triage evaluation works. Sample EMS narratives are provided in
				the application for this purpose.
			</p>
